--RIGHT JOIN: O resultado do SELECT com o RIGHT JOIN será os registros da tabela B + os registros da tabela A onde houver correspondencia, obtendo os registros que possuem
--os seguintes CODIGOS: 1, 2, 3, 5 e 8.
--FULL JOIN: O resultado do SELECT do o FULL JOIN será os todos os registros de ambas as tabelas.